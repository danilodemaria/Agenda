    private void buttonCadastrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonCadastrarMouseClicked
        // TODO add your handling code here:
        Banco conexao = new Banco();
        boolean cadastro = false;

        Cliente a = new Cliente();

        a.setNome(textNome.getText());
        a.setTelefoneFixo(textNumFixo.getText());
        a.setTelefoneCelular(textNumCel.getText());
        a.setEmail(textMail.getText());

        a = a.validaCampos(a);

        cadastro = conexao.cadastraCliente(a);

        if (cadastro == false) {
            JOptionPane.showMessageDialog(null, "Contato n√£o cadastrado.");
        } else {
            JOptionPane.showMessageDialog(null, "Contato cadastro com sucesso.");
            textNome.setText(null);
            textNumFixo.setText(null);
            textMail.setText(null);
            textNumCel.setText(null);
            textNome.requestFocus();
        }
    }//GEN-LAST:event_buttonCadastrarMouseClicked

